    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Capturamos los datos de todos los textFiles
        String matricula = txtMatricula.getText();
        String nombre = txtNombre.getText();
        int edad = Integer.parseInt(txtEdad.getText());
        String email = txtEmail.getText();
        String sexo="";
        
        //Validamos que radioButton est√° seleccionado
        if(rbMasculino.isSelected()){
            sexo= "M";
        }else if(rdFemenino.isSelected()){
            sexo="F";
        }
        
        try{
            Connection con = conexion.getconection();
            PreparedStatement ps= con.prepareStatement("INSERT INTO Alumnos(matricula,nombre,edad,sexo,email,activo) VALUES(?,?,?,?,?,?)");
            
            ps.setString(1, matricula);
            ps.setString(2, nombre);
            ps.setInt(3, edad);
            ps.setString(4, sexo);
            ps.setString(5, email);
            ps.setInt(6, 1);
            
            //Hacemos la insersion 
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Guardados exitosamente");
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

